  /// Lấy câu Lời Chúa ngẫu nhiên theo ngày (mỗi ngày 1 câu khác nhau)
  static Map<String, String> getDailyVerse() {
    final now = DateTime.now();
    // Dùng ngày trong năm làm seed để mỗi ngày ra 1 câu khác nhau
    final dayOfYear = now.difference(DateTime(now.year, 1, 1)).inDays;
    final index = dayOfYear % verses.length;
    return verses[index];
  }
